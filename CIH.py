def second_smallest(numbers):
    # this function is used to find a second station opportunity for the operators
    m1, m2 = float('inf'), float('inf')
    for x in numbers:
        if x <= m1:
            m1, m2 = x, m1
        elif x < m2:
            m2 = x
    return m2


